# 1인치는 2.54cm이므로, 이 관계식을 이용해서 인치를 센티미터로 변환할 수 있어요.
def inch_to_cm(inch):
    cm = inch * 2.54
    return cm

# 함수 사용하기
length_in_cm = inch_to_cm(10)
print(f"10인치는 {length_in_cm}cm입니다.")

