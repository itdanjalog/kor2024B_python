def repair_car(model, problem):
    # 문제 해결을 위한 작업 수행
    fixed_car = f"{model}의 {problem} 문제를 해결했습니다."
    return fixed_car

result = repair_car("소나타", "엔진 소음")
print(result)  # 출력: 소나타의 엔진 소음 문제를 해결했습니다.


